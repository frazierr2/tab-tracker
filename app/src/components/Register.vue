<template>
<div>
  <h1>Register</h1> <input type="text" name="email" placeholder="your-email@example.com" v-model="email"> <br> <input type="password" name="password" placeholder="password" v-model="password"> <br> <button @click="register">Register</button> </div>
</template>

<!-- V-MODEL (two-way binding)   -->
<script>
import AuthenticationService from '@/services/AuthenticationService'
// everything inside script tag is the controller
export default {
  name: 'Register',
  data() {
    return {
      email: '',
      password: ''
    }
  },
  methods: {
    async register() {
      const response = await AuthenticationService.register({
        email: this.email,
        password: this.password
      })
      console.log('response: ', response.data)
    }
  }
  // Once mounted run function to change email to mine
  // mounted() {}
}
</script>

